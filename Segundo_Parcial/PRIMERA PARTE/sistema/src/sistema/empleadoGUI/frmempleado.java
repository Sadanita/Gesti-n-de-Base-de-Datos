    private void btnEditarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnEditarActionPerformed
        txtid_empleado.setEditable(false);
        var.setId_empleado(txtid_empleado.getText());
        var.setNombre_empleado(txtnombre_empleado.getText());
        var.setApellido_empleado(txtapellido_empleado.getText());
        var.setEspecializacion_empleado(txtespecializacion.getText());
        objcrud.actualizar(var.getNombre_empleado(), var.getApellido_empleado(), var.getEspecializacion_empleado(),var.getId_empleado());
        txtid_empleado.setText("");
        txtnombre_empleado.setText("");
        txtapellido_empleado.setText("");
        txtespecializacion.setText("");
    }//GEN-LAST:event_btnEditarActionPerformed

    
