
package sistema.empleadoBL;

public class VariablesDeRetorno {

    public static String id_empleado;
    public static String nombre_empleado;
    public static String apellido_empleado;
    public static String especializacion_empleado;

    public String getId_empleado() {
        return id_empleado;
    }

    public void setId_empleado(String id_empleado) {
        this.id_empleado = id_empleado;
    }

    public String getNombre_empleado() {
        return nombre_empleado;
    }

    public void setNombre_empleado(String nombre_empleado) {
        this.nombre_empleado = nombre_empleado;
    }

    public String getApellido_empleado() {
        return apellido_empleado;
    }

    public void setApellido_empleado(String apellido_empleado) {
        this.apellido_empleado = apellido_empleado;
    }

    public String getEspecializacion_empleado() {
        return especializacion_empleado;
    }

    public void setEspecializacion_empleado(String especializacion_empleado) {
        this.especializacion_empleado = especializacion_empleado;
    }

}
